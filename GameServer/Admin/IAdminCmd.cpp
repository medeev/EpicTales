

#include "Pch.h"
#include "IAdminCmd.h"

#include "AdminManager.h"

////////////////////////////////////////////////////////////////////////////////////////////////////
/// @ brief		생성자
////////////////////////////////////////////////////////////////////////////////////////////////////
IAdminCommand::IAdminCommand(const std::string& cmd)
{
	AdminManager::Instance().addCommand(cmd, this);
}

////////////////////////////////////////////////////////////////////////////////////////////////////
/// @ brief		플레이어 식별자를 얻어온다
////////////////////////////////////////////////////////////////////////////////////////////////////
uint64_t IAdminCommand::getPlayerId(const std::map<std::string, int64_t>& cmdParams) const
{
	auto iter = cmdParams.find("playerId");
	if (iter == cmdParams.end())
		return 0;
	return iter->second;
}

////////////////////////////////////////////////////////////////////////////////////////////////////
/// @ brief		유저식별자를 얻어온다.
////////////////////////////////////////////////////////////////////////////////////////////////////
uint64_t IAdminCommand::getUserId(const std::map<std::string, int64_t>& cmdParams) const
{
	auto iter = cmdParams.find("userId");
	if (iter == cmdParams.end())
		return 0;
	return iter->second;
}
